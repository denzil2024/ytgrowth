/**
 * Generates frontend/src/blog/postsMeta.js from the source-of-truth
 * frontend/src/blog/posts.jsx.
 *
 * WHY: posts.jsx carries the full JSX body of all 44 articles (~1 MB). The
 * homepage blog teaser (Landing.jsx) and the /blog list (Blog.jsx) only need
 * post metadata (slug, title, excerpt, date, category, cover, author,
 * readTime, updated). Importing posts.jsx pulled the entire 1 MB of article
 * bodies onto those pages. This script extracts a tiny metadata-only module
 * so those pages import ~30 KB instead of ~1 MB.
 *
 * It is regenerated on every build (see build.js) so it can never drift from
 * posts.jsx. Do NOT hand-edit postsMeta.js.
 *
 * Extraction is text-based (no bundler): the meta fields sit on their own
 * lines before `content:` in each post object, exactly like prerender.js
 * already relies on for slug/cover discovery.
 */
import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const SRC = resolve(__dirname, '..', 'src', 'blog', 'posts.jsx')
const OUT = resolve(__dirname, '..', 'src', 'blog', 'postsMeta.js')

const STRING_FIELDS = ['slug', 'title', 'excerpt', 'date', 'cover', 'author', 'readTime', 'updated']

function field(metaText, key) {
  // Single-quoted JS string value on its own line: key: '...(escaped)...'
  const m = new RegExp(`(?:^|\\n)\\s*${key}:\\s*'((?:[^'\\\\]|\\\\.)*)'`).exec(metaText)
  return m ? m[1] : null
}

async function main() {
  const src = await readFile(SRC, 'utf-8')

  // CATEGORIES block, copied verbatim so category references resolve.
  const catMatch = /export const CATEGORIES = \{[\s\S]*?\n\}/.exec(src)
  if (!catMatch) throw new Error('[gen-blog-meta] CATEGORIES block not found')
  const categoriesBlock = catMatch[0]

  // Array body: everything between `export const posts = [` and the helper fns.
  const afterOpen = src.split('export const posts = [')[1]
  if (!afterOpen) throw new Error('[gen-blog-meta] posts array not found')
  const arrayText = afterOpen.split('\nexport function getPostBySlug')[0]

  // Each post starts with a 2-space-indented `{` on its own line.
  const blocks = arrayText.split(/\n {2}\{\n/).slice(1)
  if (blocks.length === 0) throw new Error('[gen-blog-meta] no post blocks parsed')

  const metas = []
  for (const block of blocks) {
    const metaText = block.split(/\n {4}content:/)[0] // fields only, no JSX body
    const slug = field(metaText, 'slug')
    if (!slug) throw new Error('[gen-blog-meta] a post block has no slug')
    const cat = /category:\s*(CATEGORIES\.\w+)/.exec(metaText)
    if (!cat) throw new Error(`[gen-blog-meta] post '${slug}' has no category`)
    const rec = { category: cat[1] }
    for (const k of STRING_FIELDS) {
      const v = field(metaText, k)
      if (v !== null) rec[k] = v
    }
    metas.push(rec)
  }

  // Emit. String values are re-wrapped in single quotes using their captured
  // raw form (already escaped for a single-quoted JS context).
  const order = ['slug', 'title', 'excerpt', 'date', 'category', 'cover', 'author', 'readTime', 'updated']
  const lines = metas.map((m) => {
    const parts = order
      .filter((k) => m[k] !== undefined)
      .map((k) => (k === 'category' ? `category: ${m.category}` : `${k}: '${m[k]}'`))
    return `  { ${parts.join(', ')} },`
  })

  const out = `// GENERATED by scripts/gen-blog-meta.js from posts.jsx — do NOT hand-edit.
// Metadata-only view of the blog posts so Landing + Blog list load ~30 KB
// instead of the full ~1 MB posts.jsx (which carries every article body).

${categoriesBlock}

export const postsMeta = [
${lines.join('\n')}
]

export function getPostMetaBySlug(slug) {
  return postsMeta.find((p) => p.slug === slug) || null
}

export function getRelatedPosts(currentSlug, max = 3) {
  return postsMeta.filter((p) => p.slug !== currentSlug).slice(0, max)
}

export function formatPostDate(isoDate) {
  const d = new Date(isoDate)
  return d.toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
}
`

  await writeFile(OUT, out, 'utf-8')
  console.log(`[gen-blog-meta] wrote ${metas.length} posts to ${OUT}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
